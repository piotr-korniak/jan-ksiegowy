CREATE TABLE USERS (
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   USERNAME VARCHAR(16),
   PASSWORD VARCHAR(64),

   PRIMARY KEY (ID)
);

CREATE TABLE SHARDS (
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   NAME VARCHAR(32),

   PRIMARY KEY (ID)
);